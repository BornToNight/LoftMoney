package ru.borntonight.loftmoney.remote;

public class MoneyItem {
    int id;
    String name;
    int price;
    String type;
    String date;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }

    public String getType() {
        return type;
    }

    public String getDate() {
        return date;
    }
}